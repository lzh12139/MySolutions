#include<bits/stdc++.h>
using namespace std;
double f[1000];
char s[1000010];
int main(){
	int t;scanf("%d",&t);
	while(t--){
		int n=10;double a;
		scanf("%s%lf",s+1,&a);
		if(strlen(s+1)>2)n=10;
		else if(strlen(s+1)==2)n=(s[1]-'0')*10+s[2]-'0';
		else n=s[1]-'0';
		n=min(n,10);
		f[1]=5.0/3.0;
		for(int i=2;i<=n;i++)f[i]=f[1]-1.0/(3.0*(3*f[i-1]+2.0));
		printf("%.10lf\n",a*f[n]);
	}
}
